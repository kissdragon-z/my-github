package com.zml.operators;

/**
 * java的基础运算符的操作
 */
public class OperatorsMain {

    public static void main(String[] args) {



        int a = 0;
        a++; // 前缀自增自减法(++a,--a): 先进行自增或者自减运算，再进行表达式运算。
        //后缀自增自减法(a++,a--): 先进行表达式运算，再进行自增或者自减运算 实例：

        System.out.println(a);


        int b = 0;
        ++b;
        System.out.println(b);



    }

}
